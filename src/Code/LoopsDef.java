package Code;

public class LoopsDef {

	public static void main(String[] args) {
		/*
		 * 1. What are the different types of loops in Java? Answer: Java provides three
		 * types of loops:
		 * 
		 * for loop: Executes a block of code a specific number of times. while loop:
		 * Executes a block of code as long as a condition is true. do-while loop:
		 * Executes a block of code at least once and then repeats as long as the
		 * condition is true.
		 */
		
//		For loops used when the number of iteration is known, if not unknown then used while loop
		
//		Infinite loop
		for(;;) {
			System.out.println("1");
		}
	}

}
